<template>
  <div
    :class="{
      'input-container d-flex align-center': true,
      'flex-column': !$vuetify.breakpoint.lgAndUp,
    }"
  >
    <v-progress-circular
      indeterminate
      color="amber"
      class="mr-4"
      v-if="isLoading"
    ></v-progress-circular>
    <div class="d-flex align-center">
      <v-text-field
        v-model="searchQuery"
        class="search"
        flat
        outlined
        label="Type to search"
        solo
        prepend-inner-icon="mdi-magnify"
        hide-details
      >
      </v-text-field>
      <v-btn
        fab
        class="mx-3"
        v-if="$vuetify.breakpoint.lgAndDown"
        @click="inputClick"
        elevation="2"
      >
        <v-icon color="primary">mdi-filter</v-icon>
      </v-btn>
    </div>
    <div class="text-center btn-pop">
      <v-btn rounded class="btn-pop mt-4 mt-lg-0" to="searches" color="#fcb919"
        >Popular Searches</v-btn
      >
    </div>
  </div>
</template>

<script>
export default {
  props: {
    value: {
      type: String,
      default: '',
    },
    isLoading: {
      type: Boolean,
      default: false,
    },
  },
  computed: {
    searchQuery: {
      get() {
        return this.value
      },
      set(val) {
        this.$emit('input', val)
      },
    },
  },
  methods: {
    inputClick() {
      this.$emit('click', arguments)
    },
  },
}
</script>

